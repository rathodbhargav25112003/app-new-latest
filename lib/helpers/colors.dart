  static Future<void> saveTheme() async {
    SharedPreferences prefs = await SharedPreferences.getInstance();
    prefs.setString('theme', currentTheme.toString());
  }

  // Retrieve the saved theme from shared preferences
  static Future<void> loadTheme() async {
    SharedPreferences prefs = await SharedPreferences.getInstance();
    String theme = prefs.getString('theme') ?? AppTheme.Light.toString();
    currentTheme =
        theme == AppTheme.Light.toString() ? AppTheme.Light : AppTheme.Dark;
  }

  // Toggle between light and dark themes
  static void toggleTheme() {
    currentTheme =
        (currentTheme == AppTheme.Light) ? AppTheme.Dark : AppTheme.Light;
    saveTheme();
    print('Current Theme: $currentTheme');
  }
}

class ThemeNotifier with ChangeNotifier {
  ThemeNotifier() {
    ThemeManager.loadTheme();
  }

  AppTheme get currentTheme => ThemeManager.currentTheme;

  void toggleTheme() {
    ThemeManager.toggleTheme();
    notifyListeners();
  }
}
